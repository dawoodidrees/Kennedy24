const signMessage2 = async (
  web3Instance: any,
  nonce: number,
  address: string,
  contractAddress: string
) => {
  const message = web3Instance.utils.soliditySha3(
    contractAddress,
    address,
    nonce
  );
  console.log("message", message);

  var { r, s, v } = await web3Instance.eth.accounts.sign(
    message,
    publicSignerPrivateKey
  );

  return { r, s, v: parseInt(v, 16) };
};
