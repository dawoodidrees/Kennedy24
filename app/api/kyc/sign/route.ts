export async function POST(request: NextRequest) {
  const body = await request.json();
  console.log({ body });
  const { collectionId, address } = body;
  console.log({ collectionId });
  const collection = await prisma.tCSCampaign.findUnique({
    where: {
      id: collectionId,
    },
  });
  if (collection) {
    const web3Instance = new Web3(alchemyProvider);

    const contractInstance = new web3Instance.eth.Contract(
      abi,
      collection.contract_address
    );
    const nonce = (await contractInstance.methods
      .nonce(address)
      .call()) as number;

    const signature = await signMessage2(
      web3Instance,
      nonce,
      address,
      collection.contract_address
    );

    return new Response(JSON.stringify({ signature }), { status: 200 });
  }
  return new Response(JSON.stringify({ error: "Collection not found" }), {
    status: 404,
  });
}

